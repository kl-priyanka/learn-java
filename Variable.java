public class Variable {
    public static void main(String[] args){
        int a = 5;
        int b = 15;
        int result = a + b;
        System.out.println(result);
        float marks = 68.5f;
        System.out.println(marks);
        byte character = 'f';
        char c = (char) character;
        System.out.println(character);
        System.out.println(c);
        System.out.println(22/7);
    }
}
